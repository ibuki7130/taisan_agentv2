function getToneConfig() {
  const hour = new Date().getHours();
  if (hour >= 5 && hour < 11) return TONE_BY_HOUR.morning;
  if (hour >= 11 && hour < 17) return TONE_BY_HOUR.noon;
  return TONE_BY_HOUR.night;
}

function getRandom(arr) {
  return arr[Math.floor(Math.random() * arr.length)];
}

// 連続重複防止
let lastPersonaIndex = -1;
let lastThemeIndex = -1;

function getNextPersona() {
  let idx;
  do { idx = Math.floor(Math.random() * PERSONAS.length); }
  while (idx === lastPersonaIndex);
  lastPersonaIndex = idx;
  return PERSONAS[idx];
}

function getNextTheme() {
  // HOTテーマ（score 0.80以上）を70%の確率で選択
  const hotThemes = THEMES.filter(t => t.score >= 0.80);
  const useHot = Math.random() < 0.70;
  const pool = useHot ? hotThemes : THEMES;

  let idx;
  do { idx = Math.floor(Math.random() * pool.length); }
  while (pool === THEMES && idx === lastThemeIndex);

  if (pool === THEMES) lastThemeIndex = idx;
  return pool[idx];
}

